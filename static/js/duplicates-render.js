async function renderDuplicates() {
    const pc = $('page-content');
    pc.innerHTML = `<div class="skeleton" style="height:300px;border-radius:12px"></div>`;

    if (!state.dupFilters) {
        state.dupFilters = { sortBy: 'date_desc', fileType: '', location: '', mode: 'exact' };
    }

    try {
        const data = await API.getDuplicates(
            state.dupFilters.sortBy,
            state.dupFilters.fileType,
            state.dupFilters.location,
            state.dupFilters.mode
        );

        const controlsHtml = renderDupControlsBar(data.groups || []);
        const emptyHint = state.dupFilters.mode === 'visual'
            ? t('duplicates_render.empty_hint_visual')
            : t('duplicates_render.empty_hint_exact');

        if (!data.groups || !data.groups.length) {
            pc.innerHTML = controlsHtml + renderEmptyState(t('duplicates_render.no_duplicates_title'), emptyHint);
            bindDupFilters();
            // The scan-trigger button lives in controlsHtml, rendered on
            // this empty-state path too - without this, clicking it did
            // nothing at all (no handler was ever attached).
            bindDupActions([]);
            return;
        }

        const groupsHtml = data.groups.map((group, idx) => renderDupGroup(group, idx)).join('');

        pc.innerHTML = controlsHtml + `<div class="dup-groups-list">${groupsHtml}</div>`;
        bindDupFilters();
        bindDupActions(data.groups);

    } catch (e) {
        pc.innerHTML = renderEmptyState(t('duplicates_render.error_occurred'), e.message);
    }
}

function renderDupControlsBar(groups) {
    const f = state.dupFilters;
    // Same "keep the best-quality one, drop the rest" count auto-clean-all
    // itself uses (see duplicates-actions.js's _bestQualityFirst) - shown
    // up front so the count on the button matches exactly what clicking it
    // is about to remove, not just the number of duplicate groups.
    const removableCount = groups.reduce((sum, g) => sum + Math.max(0, g.assets.length - 1), 0);
    const autoCleanLabel = removableCount > 0
        ? t('duplicates_render.auto_clean_all_count', { count: removableCount })
        : t('duplicates_render.auto_clean_all');
    return `
        <div class="dup-controls-bar">
            <div class="dup-mode-tabs">
                <button class="dup-mode-tab ${f.mode === 'exact' ? 'active' : ''}" data-mode="exact">
                    ${t('duplicates_render.mode_exact')} <span class="dup-mode-tab-hint">${t('duplicates_render.mode_exact_hint')}</span>
                </button>
                <button class="dup-mode-tab ${f.mode === 'visual' ? 'active' : ''}" data-mode="visual">
                    ${t('duplicates_render.mode_visual')} <span class="dup-mode-tab-hint">${t('duplicates_render.mode_visual_hint')}</span>
                </button>
            </div>
            <div class="dup-controls-filters">
                <div class="dup-control-group">
                    <label>${t('duplicates_render.sort_label')}</label>
                    <select id="dup-sort-by">
                        <option value="date_desc" ${f.sortBy === 'date_desc' ? 'selected' : ''}>${t('duplicates_render.sort_date_desc')}</option>
                        <option value="date_asc" ${f.sortBy === 'date_asc' ? 'selected' : ''}>${t('duplicates_render.sort_date_asc')}</option>
                        <option value="size_desc" ${f.sortBy === 'size_desc' ? 'selected' : ''}>${t('duplicates_render.sort_size_desc')}</option>
                        <option value="name" ${f.sortBy === 'name' ? 'selected' : ''}>${t('duplicates_render.sort_name')}</option>
                    </select>
                </div>
                <div class="dup-control-group">
                    <label>${t('duplicates_render.type_label')}</label>
                    <select id="dup-file-type">
                        <option value="" ${f.fileType === '' ? 'selected' : ''}>${t('duplicates_render.type_all')}</option>
                        <option value="IMAGE" ${f.fileType === 'IMAGE' ? 'selected' : ''}>${t('duplicates_render.type_images')}</option>
                        <option value="VIDEO" ${f.fileType === 'VIDEO' ? 'selected' : ''}>${t('duplicates_render.type_videos')}</option>
                    </select>
                </div>
                <div class="dup-control-group">
                    <label>${t('duplicates_render.location_filter_label')}</label>
                    <input id="dup-location" type="text" placeholder="${t('duplicates_render.location_placeholder')}" value="${escHtml(f.location)}">
                </div>
            </div>
            <div class="dup-controls-actions">
                <button id="dup-scan-trigger" class="btn btn-secondary"><span>🔍</span> ${t('duplicates_render.scan_trigger')}</button>
                <button id="dup-slideshow-start" class="btn btn-secondary"><span>🎬</span> ${t('duplicates_render.slideshow_start')}</button>
                <button id="dup-auto-clean-all" class="dup-auto-clean-all-btn"><span>🧹</span> ${autoCleanLabel}</button>
            </div>
        </div>
    `;
}

function renderDupGroup(group, idx) {
    const totalSizeStr = formatSize(group.total_size);
    const assetsHtml = group.assets.map(a => renderDupAssetCard(a)).join('');
    const isVisual = group.match_score != null;
    const matchInfo = isVisual
        ? `${t('duplicates_render.similarity_label')} <strong>%${group.match_score}</strong>`
        : `${t('duplicates_render.hash_label')} <code>${group.checksum.substring(0, 16)}</code>`;
    // Visual groups skip by member asset ids (see IgnoredSimilarAsset) since
    // the group's own hash shifts whenever the cluster gains/loses a member -
    // an exact-match group is a stable checksum, so it skips by that instead.
    const skipAttr = isVisual
        ? `data-asset-ids="${group.assets.map(a => a.id).join(',')}"`
        : `data-checksum="${group.checksum}"`;

    return `
        <div class="dup-group-container">
            <div class="dup-group-header">
                <div>
                    <span class="dup-group-title">${t('duplicates_render.group_number', { num: idx + 1 })}</span>
                    <span class="dup-group-meta">${t('duplicates_render.group_size_label')} ${totalSizeStr} | ${matchInfo}</span>
                </div>
                <div class="dup-group-header-actions">
                    <button class="btn-group-skip" ${skipAttr}>⏭ ${t('duplicates_render.skip')}</button>
                    <button class="btn-group-auto-clean" data-checksum="${group.checksum}">${t('duplicates_render.clean_group')}</button>
                    <button class="btn-group-delete-all" data-checksum="${group.checksum}">🗑 ${t('duplicates_render.delete_all')}</button>
                </div>
            </div>
            <div class="dup-group-assets">${assetsHtml}</div>
        </div>
    `;
}

function renderDupAssetCard(a) {
    const thumb = a.id ? API.getThumb(a.id, 'medium') : '';
    const sizeStr = formatSize(a.file_size);
    const locale = _DATE_LOCALES[getLanguage()];
    const dateStr = a.taken_at ? new Date(a.taken_at).toLocaleString(locale) : new Date(a.created_at).toLocaleString(locale);
    const resolution = a.width && a.height ? `${a.width} × ${a.height}` : '';
    const locationStr = [a.city, a.country].filter(Boolean).join(', ') || t('duplicates_render.unknown_location');
    const fileTypeLabel = a.file_type === 'VIDEO' ? `🎥 ${t('duplicates_render.file_type_video')}` : `🖼 ${t('duplicates_render.file_type_image')}`;

    return `
        <div class="dup-asset-card" data-id="${a.id}">
            <div class="dup-asset-thumb" data-id="${a.id}">
                <img src="${thumb}" alt="" onerror="this.onerror=null;this.src='/static/broken-file.png'">
                <div class="dup-asset-type-badge">${fileTypeLabel}</div>
            </div>
            <div class="dup-asset-body">
                <div class="dup-asset-info">
                    <div class="dup-asset-name" title="${escHtml(a.file_name)}">${escHtml(a.file_name)}</div>
                    <div class="dup-asset-meta"><strong>${t('duplicates_render.size_label')}</strong> ${sizeStr}</div>
                    ${resolution ? `<div class="dup-asset-meta"><strong>${t('duplicates_render.resolution_label')}</strong> ${resolution}</div>` : ''}
                    <div class="dup-asset-meta"><strong>${t('duplicates_render.date_label')}</strong> ${dateStr}</div>
                    <div class="dup-asset-meta"><strong>${t('duplicates_render.location_label')}</strong> 📍 ${locationStr}</div>
                </div>
                <button class="btn-trash-dup" data-id="${a.id}">${t('duplicates_render.trash_this_copy')}</button>
            </div>
        </div>
    `;
}
