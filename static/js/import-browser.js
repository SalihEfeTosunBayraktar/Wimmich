async function browsePath(path) {
    const results = $('browse-results');
    if (!results) return;
    results.innerHTML = '<div class="skeleton" style="height:200px"></div>';

    // A real drive can have thousands of entries in a single folder, and
    // this whole panel previously gave zero indication anything was
    // happening beyond the skeleton bar above - clicking a folder row (or
    // Go) repeatedly, thinking it hadn't registered, was a completely
    // reasonable reaction to that silence.
    const goBtn = $('browse-go-btn');
    const prevGoText = goBtn ? goBtn.textContent : null;
    if (goBtn) { goBtn.disabled = true; goBtn.textContent = t('import_browser.browsing_btn'); }

    try {
        const data = await API.browsePath(path || '');
        $('browse-path').value = data.current_path || '';

        let html = '';
        if (data.parent_path !== null && data.parent_path !== undefined) {
            html += `<div class="browse-item browse-item--parent" onclick="browsePath('${escAttr(data.parent_path)}')">
                <span>📁</span> <span>${t('import_browser.parent_folder')}</span>
            </div>`;
        }

        data.items.forEach(item => {
            if (item.type === 'folder' || item.type === 'drive') {
                html += `<div class="browse-item browse-item--dir" onclick="browsePath('${escAttr(item.path)}')">
                    <span>${item.type === 'drive' ? '💾' : '📁'}</span>
                    <span style="flex:1;font-weight:500">${escHtml(item.name)}</span>
                    ${item.media_count !== undefined ? `<span style="font-size:0.8rem;color:var(--text-muted)">${t('import_browser.media_count', { count: item.media_count })}</span>` : ''}
                    ${item.total_size ? `<span style="font-size:0.8rem;color:var(--text-muted)">${formatSize(item.free_size)} ${t('import_browser.free_suffix')}</span>` : ''}
                </div>`;
            } else {
                html += `<div class="browse-item browse-item--file">
                    <span>${item.media_type === 'VIDEO' ? '🎬' : '🖼'}</span>
                    <span style="flex:1;font-size:0.9rem">${escHtml(item.name)}</span>
                    <span style="font-size:0.8rem;color:var(--text-muted)">${formatSize(item.size)}</span>
                </div>`;
            }
        });

        if (!data.items.length) {
            html = `<div style="padding:24px;text-align:center;color:var(--text-muted)">${t('import_browser.no_media_found')}</div>`;
        }

        results.innerHTML = html;
    } catch (e) {
        results.innerHTML = `<div style="padding:16px;color:var(--danger)">${e.message}</div>`;
    } finally {
        if (goBtn) { goBtn.disabled = false; goBtn.textContent = prevGoText; }
    }
}

async function scanImportPath() {
    const path = $('browse-path').value.trim();
    if (!path) { toast(t('import_browser.enter_path'), 'warning'); return; }

    const copyFiles = $('import-copy').checked;
    const recursive = $('import-recursive').checked;
    const sr = $('scan-results');
    // Same reasoning as browsePath's Go button - a recursive scan over a
    // real external drive can take a while, and a static skeleton with no
    // further change for that whole stretch reads as frozen, not busy.
    sr.innerHTML = `<div class="skeleton" style="height:36px;border-radius:8px;margin-bottom:8px"></div><p class="text-muted" style="margin:0;font-size:0.85rem">${t('import_browser.scanning_hint')}</p>`;
    const scanBtn = $('scan-import-btn');
    const prevScanText = scanBtn ? scanBtn.textContent : null;
    if (scanBtn) { scanBtn.disabled = true; scanBtn.textContent = t('import_browser.scanning_btn'); }

    try {
        const data = await API.scanFolder(path, copyFiles, recursive);
        sr.innerHTML = `
            <div style="background:var(--bg-surface);border:1px solid var(--border-color);border-radius:10px;padding:16px">
                <h4 style="margin-bottom:8px">${t('import_browser.scan_result_title')}</h4>
                <div style="display:grid;grid-template-columns:repeat(auto-fill,minmax(150px,1fr));gap:8px;margin-bottom:12px">
                    <div><span style="color:var(--text-muted)">${t('import_browser.total_label')}</span> <strong>${data.total_found}</strong></div>
                    <div><span style="color:var(--text-muted)">${t('import_browser.new_label')}</span> <strong style="color:var(--success)">${data.new_files}</strong></div>
                    <div><span style="color:var(--text-muted)">${t('import_browser.existing_label')}</span> <strong>${data.already_imported}</strong></div>
                    <div><span style="color:var(--text-muted)">${t('import_browser.photos_label')}</span> <strong>${data.images}</strong></div>
                    <div><span style="color:var(--text-muted)">${t('import_browser.video_label')}</span> <strong>${data.videos}</strong></div>
                    <div><span style="color:var(--text-muted)">${t('import_browser.size_label')}</span> <strong>${formatSize(data.total_size)}</strong></div>
                </div>
                ${data.new_files > 0 ? `
                    <button class="btn btn-primary" onclick="startImport('${escAttr(path)}')">
                        🚀 ${t('import_browser.import_files_button', { count: data.new_files })} (${data.copy_mode ? t('import_browser.copy_mode') : t('import_browser.reference_mode')})
                    </button>
                ` : `<p style="color:var(--text-muted)">${t('import_browser.no_new_files')}</p>`}
            </div>
        `;
    } catch (e) {
        sr.innerHTML = `<div style="padding:12px;color:var(--danger)">${e.message}</div>`;
    } finally {
        if (scanBtn) { scanBtn.disabled = false; scanBtn.textContent = prevScanText; }
    }
}

const IMPORT_JOB_STORAGE_KEY = 'wimmich_active_import_job';

function _renderImportProgressBar(jobId) {
    const sr = $('scan-results');
    if (!sr) return;
    sr.innerHTML = `
        <div style="background:rgba(34,197,94,0.1);border:1px solid rgba(34,197,94,0.2);border-radius:10px;padding:16px">
            <p style="color:var(--success);font-weight:600">${t('import_browser.import_in_progress')}</p>
            <p style="color:var(--text-secondary);font-size:0.85rem;margin-top:4px">${t('import_browser.job_id_label', { id: jobId })}</p>
            <div id="import-progress-bar" style="margin-top:8px">
                <div class="upload-progress-bar"><div class="upload-progress-fill" id="import-fill" style="width:0%"></div></div>
                <span id="import-percent" style="font-size:0.8rem;color:var(--text-muted)">0%</span>
            </div>
        </div>
    `;
}

async function startImport(path) {
    const copyFiles = $('import-copy')?.checked ?? true;
    const recursive = $('import-recursive')?.checked ?? true;

    try {
        const result = await API.startImport(path, copyFiles, recursive);
        toast(t('import_browser.import_started'), 'success');
        localStorage.setItem(IMPORT_JOB_STORAGE_KEY, result.job_id);
        _renderImportProgressBar(result.job_id);
        pollImportProgress(result.job_id);
    } catch (e) { toast(e.message, 'error'); }
}

// The admin page (and its progress bar) is fully re-rendered from scratch on
// every navigation, so without this an in-progress import "loses" its
// progress bar the moment you leave the page and come back - the job is
// still running server-side, the UI just forgot about it. Called once when
// the admin page mounts.
async function resumeImportProgressIfActive() {
    const jobId = localStorage.getItem(IMPORT_JOB_STORAGE_KEY);
    if (!jobId) return;

    try {
        const s = await API.getImportStatus(jobId);
        if (s.status === 'PENDING' || s.status === 'RUNNING') {
            _renderImportProgressBar(jobId);
            pollImportProgress(jobId);
        } else {
            localStorage.removeItem(IMPORT_JOB_STORAGE_KEY);
        }
    } catch {
        localStorage.removeItem(IMPORT_JOB_STORAGE_KEY);
    }
}

async function pollImportProgress(jobId) {
    const interval = setInterval(async () => {
        try {
            const s = await API.getImportStatus(jobId);
            const fill = $('import-fill');
            const pct = $('import-percent');
            if (fill) fill.style.width = s.progress + '%';
            if (pct) pct.textContent = s.progress + '%';

            if (s.status === 'COMPLETED' || s.status === 'FAILED') {
                clearInterval(interval);
                localStorage.removeItem(IMPORT_JOB_STORAGE_KEY);
                if (s.status === 'COMPLETED') {
                    toast(t('import_browser.import_completed'), 'success');
                    if (pct) pct.textContent = t('import_browser.completed_label');
                    refreshReferenceRootsList();
                } else {
                    toast(t('import_browser.import_failed_prefix') + (s.error || ''), 'error');
                }
            }
        } catch { clearInterval(interval); }
    }, IMPORT_POLL_INTERVAL_MS);
}

function renderReferenceRootsList(roots) {
    if (!roots || !roots.length) {
        return `<p class="text-muted admin-field-hint" style="margin:0">${t('admin_render.reference_roots_empty')}</p>`;
    }
    return `
        <div style="display:flex;flex-direction:column;gap:8px">
            ${roots.map(r => `
                <div class="browse-item" style="display:flex;align-items:center;gap:8px">
                    <span>🔗</span>
                    <span style="flex:1;font-size:0.9rem;word-break:break-all">${escHtml(r.path)}</span>
                    <span style="font-size:0.8rem;color:var(--text-muted);white-space:nowrap">${t('admin_render.reference_roots_item_count', { count: r.asset_count })} · ${formatSize(r.total_size)}</span>
                    <button class="btn btn-danger btn-sm" onclick="removeReferenceRoot('${escAttr(r.path)}')">${t('admin_render.reference_roots_remove_btn')}</button>
                </div>
            `).join('')}
        </div>
    `;
}

async function refreshReferenceRootsList() {
    const container = $('reference-roots-list');
    if (!container) return;
    try {
        const data = await API.getReferenceRoots();
        container.innerHTML = renderReferenceRootsList(data.references);
    } catch { /* leave the previous list showing rather than blank it on a transient error */ }
}

async function removeReferenceRoot(path) {
    try {
        const result = await API.removeReferenceRoot(path);
        toast(t('admin_render.reference_roots_removed_toast', { count: result.count }), 'success');
        refreshReferenceRootsList();
    } catch (e) { toast(e.message, 'error'); }
}
