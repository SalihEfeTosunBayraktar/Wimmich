async function browsePath(path) {
    const results = $('browse-results');
    if (!results) return;
    results.innerHTML = '<div class="skeleton" style="height:200px"></div>';

    // A real drive can have thousands of entries in a single folder, and
    // this whole panel previously gave zero indication anything was
    // happening beyond the skeleton bar above - clicking a folder row (or
    // Go) repeatedly, thinking it hadn't registered, was a completely
    // reasonable reaction to that silence.
    const goBtn = $('browse-go-btn');
    const prevGoText = goBtn ? goBtn.textContent : null;
    if (goBtn) { goBtn.disabled = true; goBtn.textContent = t('import_browser.browsing_btn'); }

    try {
        const data = await API.browsePath(path || '');
        $('browse-path').value = data.current_path || '';

        let html = '';
        if (data.parent_path !== null && data.parent_path !== undefined) {
            html += `<div class="browse-item browse-item--parent" onclick="browsePath('${escAttr(data.parent_path)}')">
                <span>📁</span> <span>${t('import_browser.parent_folder')}</span>
            </div>`;
        }

        data.items.forEach(item => {
            if (item.type === 'folder' || item.type === 'drive') {
                html += `<div class="browse-item browse-item--dir" onclick="browsePath('${escAttr(item.path)}')">
                    <span>${item.type === 'drive' ? '💾' : '📁'}</span>
                    <span style="flex:1;font-weight:500">${escHtml(item.name)}</span>
                    ${item.media_count !== undefined ? `<span style="font-size:0.8rem;color:var(--text-muted)">${t('import_browser.media_count', { count: item.media_count })}</span>` : ''}
                    ${item.total_size ? `<span style="font-size:0.8rem;color:var(--text-muted)">${formatSize(item.free_size)} ${t('import_browser.free_suffix')}</span>` : ''}
                </div>`;
            } else {
                html += `<div class="browse-item browse-item--file">
                    <span>${item.media_type === 'VIDEO' ? '🎬' : '🖼'}</span>
                    <span style="flex:1;font-size:0.9rem">${escHtml(item.name)}</span>
                    <span style="font-size:0.8rem;color:var(--text-muted)">${formatSize(item.size)}</span>
                </div>`;
            }
        });

        if (!data.items.length) {
            html = `<div style="padding:24px;text-align:center;color:var(--text-muted)">${t('import_browser.no_media_found')}</div>`;
        }

        results.innerHTML = html;
    } catch (e) {
        results.innerHTML = `<div style="padding:16px;color:var(--danger)">${e.message}</div>`;
    } finally {
        if (goBtn) { goBtn.disabled = false; goBtn.textContent = prevGoText; }
    }
}

// A scan is a single request, not a background job - there's nothing
// server-side to poll/resume after a refresh the way IMPORT jobs can be.
// Caches the RESULT itself (not just "a scan was requested"), so a refresh
// that lands after the scan already finished - by far the common case now
// that scanning is fast (see the N+1 query fix) - redraws instantly from
// the cached result instead of the panel just going blank. `result: null`
// specifically means "was still in flight when last saved," the one case
// where redoing the request is the only option.
const ACTIVE_SCAN_STORAGE_KEY = 'wimmich_active_scan';

// disk_type from the scan response (see filesystem_browse_service.py) ->
// the translated label used inside reference_risky_warning's {diskType}.
// 'fixed'/'unknown'/'ramdisk' never reach this - only called when
// reference_risky is already true, which is exactly the removable/
// network/cdrom set.
function _diskTypeLabel(diskType) {
    return t(`import_browser.disk_type_${diskType}`) || diskType;
}

function _renderScanResult(path, data) {
    const sr = $('scan-preview-slot');
    if (!sr) return;
    // Only worth warning about when Reference mode is what's actually
    // selected - Copy mode already makes its own durable copy, so the
    // source drive's reliability doesn't matter for it.
    const showRiskyWarning = data.reference_risky && !data.copy_mode;
    sr.innerHTML = `
        <div style="background:var(--bg-surface);border:1px solid var(--border-color);border-radius:10px;padding:16px">
            <h4 style="margin-bottom:8px">${t('import_browser.scan_result_title')}</h4>
            <div style="display:grid;grid-template-columns:repeat(auto-fill,minmax(150px,1fr));gap:8px;margin-bottom:12px">
                <div><span style="color:var(--text-muted)">${t('import_browser.total_label')}</span> <strong>${data.total_found}</strong></div>
                <div><span style="color:var(--text-muted)">${t('import_browser.new_label')}</span> <strong style="color:var(--success)">${data.new_files}</strong></div>
                <div><span style="color:var(--text-muted)">${t('import_browser.existing_label')}</span> <strong>${data.already_imported}</strong></div>
                <div><span style="color:var(--text-muted)">${t('import_browser.photos_label')}</span> <strong>${data.images}</strong></div>
                <div><span style="color:var(--text-muted)">${t('import_browser.video_label')}</span> <strong>${data.videos}</strong></div>
                <div><span style="color:var(--text-muted)">${t('import_browser.size_label')}</span> <strong>${formatSize(data.total_size)}</strong></div>
            </div>
            ${showRiskyWarning ? `
                <p style="color:var(--warning);font-size:0.85rem;margin-bottom:12px">
                    ${t('import_browser.reference_risky_warning', { diskType: _diskTypeLabel(data.disk_type) })}
                </p>
            ` : ''}
            ${data.new_files > 0 ? `
                <button class="btn btn-primary" onclick="startImport('${escAttr(path)}')">
                    🚀 ${t('import_browser.import_files_button', { count: data.new_files })} (${data.copy_mode ? t('import_browser.copy_mode') : t('import_browser.reference_mode')})
                </button>
            ` : `<p style="color:var(--text-muted)">${t('import_browser.no_new_files')}</p>`}
        </div>
    `;
}

async function scanImportPath() {
    const path = $('browse-path').value.trim();
    if (!path) { toast(t('import_browser.enter_path'), 'warning'); return; }

    const copyFiles = $('import-copy').checked;
    const recursive = $('import-recursive').checked;
    const sr = $('scan-preview-slot');
    // Same reasoning as browsePath's Go button - a recursive scan over a
    // real external drive can take a while, and a static skeleton with no
    // further change for that whole stretch reads as frozen, not busy.
    sr.innerHTML = `<div class="skeleton" style="height:36px;border-radius:8px;margin-bottom:8px"></div><p class="text-muted" style="margin:0;font-size:0.85rem">${t('import_browser.scanning_hint')}</p>`;
    const scanBtn = $('scan-import-btn');
    const prevScanText = scanBtn ? scanBtn.textContent : null;
    if (scanBtn) { scanBtn.disabled = true; scanBtn.textContent = t('import_browser.scanning_btn'); }
    localStorage.setItem(ACTIVE_SCAN_STORAGE_KEY, JSON.stringify({ path, copyFiles, recursive, result: null }));

    try {
        const data = await API.scanFolder(path, copyFiles, recursive);
        _renderScanResult(path, data);
        localStorage.setItem(ACTIVE_SCAN_STORAGE_KEY, JSON.stringify({ path, copyFiles, recursive, result: data }));
    } catch (e) {
        sr.innerHTML = `<div style="padding:12px;color:var(--danger)">${e.message}</div>`;
        localStorage.removeItem(ACTIVE_SCAN_STORAGE_KEY); // nothing worth restoring after a failed scan
    } finally {
        if (scanBtn) { scanBtn.disabled = false; scanBtn.textContent = prevScanText; }
    }
}

// Called once when the admin page mounts, alongside resumeImportProgressIfActive().
function resumeScanIfActive() {
    let saved;
    try {
        saved = JSON.parse(localStorage.getItem(ACTIVE_SCAN_STORAGE_KEY) || 'null');
    } catch {
        saved = null;
    }
    if (!saved || !saved.path) {
        localStorage.removeItem(ACTIVE_SCAN_STORAGE_KEY);
        return false;
    }
    // Caller skips its own browsePath('') root-listing call when this
    // returns true - both write into #browse-path, and the root listing
    // resolving after this would otherwise stomp the restored path back to
    // blank while the resumed scan is still running.
    $('browse-path').value = saved.path;
    $('import-copy').checked = saved.copyFiles;
    $('import-recursive').checked = saved.recursive;
    if (saved.result) {
        // The scan had already finished before the refresh - redraw
        // instantly from the cached result, no need to hit the server again.
        _renderScanResult(saved.path, saved.result);
    } else {
        // Refreshed while the request was still genuinely in flight - the
        // only option is to redo it.
        scanImportPath();
    }
    return true;
}

// Holds a JSON array of {jobId, path} - not a single job ID - so more than
// one folder can be imported without the second one clobbering the first's
// tracked progress. Each entry gets its own card + its own polling
// interval; they run independently regardless of how many are active.
const IMPORT_JOB_STORAGE_KEY = 'wimmich_active_import_job';

function _getActiveImports() {
    try {
        const raw = JSON.parse(localStorage.getItem(IMPORT_JOB_STORAGE_KEY) || '[]');
        return Array.isArray(raw) ? raw : [];
    } catch {
        // Leftover pre-multi-import value (a bare job ID string) or corrupt
        // JSON - either way, nothing salvageable, start clean.
        return [];
    }
}

function _setActiveImports(entries) {
    localStorage.setItem(IMPORT_JOB_STORAGE_KEY, JSON.stringify(entries));
}

function _renderActiveImportsList(entries) {
    const sr = $('active-imports-slot');
    if (!sr) return;
    sr.innerHTML = entries.map(({ jobId, path }) => `
        <div style="background:rgba(34,197,94,0.1);border:1px solid rgba(34,197,94,0.2);border-radius:10px;padding:16px;margin-bottom:8px">
            <p style="color:var(--success);font-weight:600">${t('import_browser.import_in_progress')}</p>
            <p style="color:var(--text-secondary);font-size:0.85rem;margin-top:4px">${t('import_browser.job_id_label', { id: jobId })}</p>
            ${path ? `<p style="color:var(--text-secondary);font-size:0.85rem;margin-top:2px">${t('import_browser.import_path_label', { path: escHtml(path) })}</p>` : ''}
            <div style="margin-top:8px">
                <div class="upload-progress-bar"><div class="upload-progress-fill" id="import-fill-${jobId}" style="width:0%"></div></div>
                <span id="import-percent-${jobId}" style="font-size:0.8rem;color:var(--text-muted)">0%</span>
            </div>
        </div>
    `).join('');
}

async function startImport(path) {
    const copyFiles = $('import-copy')?.checked ?? true;
    const recursive = $('import-recursive')?.checked ?? true;
    const destPath = $('import-dest-path')?.value.trim() || null;

    try {
        const result = await API.startImport(path, copyFiles, recursive, destPath);
        toast(t('import_browser.import_started'), 'success');
        // The scan result that led here is now stale (some/all of it is
        // about to be imported) - stop offering to restore it on the next
        // refresh, the import job's own progress tracking takes over from here.
        localStorage.removeItem(ACTIVE_SCAN_STORAGE_KEY);
        const entries = _getActiveImports();
        entries.push({ jobId: result.job_id, path });
        _setActiveImports(entries);
        _renderActiveImportsList(entries);
        pollImportProgress(result.job_id);
    } catch (e) { toast(e.message, 'error'); }
}

// The admin page (and its progress cards) is fully re-rendered from scratch
// on every navigation, so without this in-progress imports "lose" their
// progress bars the moment you leave the page and come back - the jobs are
// still running server-side, the UI just forgot about them. Called once
// when the admin page mounts.
async function resumeImportProgressIfActive() {
    const entries = _getActiveImports();
    if (!entries.length) return;

    const stillActive = [];
    for (const entry of entries) {
        try {
            const s = await API.getImportStatus(entry.jobId);
            if (s.status === 'PENDING' || s.status === 'RUNNING') {
                stillActive.push({ jobId: entry.jobId, path: entry.path || s.path });
            } else if (s.status === 'FAILED' && s.retried_as) {
                // The hang watchdog abandoned this exact attempt and
                // auto-requeued it under a new id - follow the chain
                // instead of treating a still-running import as failed.
                stillActive.push({ jobId: s.retried_as, path: entry.path });
            }
            // COMPLETED / CANCELLED / a real FAILED (no retry) - confirmed
            // terminal, correctly falls out of tracking here.
        } catch {
            // Request failed (network blip, brief server hiccup, SQLite
            // briefly busy mid-commit) - NOT confirmed evidence the job is
            // gone. Keep tracking it optimistically instead of silently
            // forgetting a real, still-running import; the next poll tick
            // or page mount reconciles once the request actually succeeds.
            stillActive.push(entry);
        }
    }
    _setActiveImports(stillActive);
    if (!stillActive.length) return;
    _renderActiveImportsList(stillActive);
    stillActive.forEach(({ jobId }) => pollImportProgress(jobId));
}

async function pollImportProgress(jobId) {
    const interval = setInterval(async () => {
        try {
            const s = await API.getImportStatus(jobId);

            if (s.status === 'FAILED' && s.retried_as) {
                // Same chain-following as resumeImportProgressIfActive()
                // above - swap to the new id, same card/slot, no
                // interruption visible to the user.
                clearInterval(interval);
                const entries = _getActiveImports().map(e => e.jobId === jobId ? { ...e, jobId: s.retried_as } : e);
                _setActiveImports(entries);
                _renderActiveImportsList(entries);
                pollImportProgress(s.retried_as);
                return;
            }

            const fill = $(`import-fill-${jobId}`);
            const pct = $(`import-percent-${jobId}`);
            if (fill) fill.style.width = s.progress + '%';
            if (pct) pct.textContent = s.progress + '%';

            if (s.status === 'COMPLETED' || s.status === 'FAILED') {
                clearInterval(interval);
                const remaining = _getActiveImports().filter(e => e.jobId !== jobId);
                _setActiveImports(remaining);
                _renderActiveImportsList(remaining);
                if (s.status === 'COMPLETED') {
                    toast(t('import_browser.import_completed'), 'success');
                    refreshReferenceRootsList();
                } else {
                    toast(t('import_browser.import_failed_prefix') + (s.error || ''), 'error');
                }
            }
        } catch {
            // Transient failure, not confirmed evidence the job is done or
            // gone - leave the interval running and the card as-is, the
            // next tick retries. Previously this cleared the interval on
            // ANY error, silently freezing (and effectively abandoning)
            // the progress bar even for a perfectly healthy import that
            // just hit one bad request.
        }
    }, IMPORT_POLL_INTERVAL_MS);
}

