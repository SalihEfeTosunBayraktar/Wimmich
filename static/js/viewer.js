async function openViewer(assetId) {
    const overlay = $('viewer-overlay');
    overlay.classList.remove('hidden');
    document.body.style.overflow = 'hidden';

    // Cleared synchronously (before the asset even loads) so the previous
    // photo's similar-badge/strip never flashes onto the new one while the
    // fetch for it is still in flight.
    _viewerSimilarAssets = [];
    _updateSimilarBadge([]);

    try {
        const asset = await API.getAsset(assetId);
        state.viewerAsset = asset;
        renderViewerMedia(asset);
        updateViewerControls(asset);
        // The info sidebar (EXIF, people, similar photos...) otherwise kept
        // showing the previous photo's data after navigating via prev/next
        // or a similar-photos click while it was already open.
        const sidebar = $('viewer-sidebar');
        if (sidebar && !sidebar.classList.contains('hidden')) {
            renderViewerInfo(asset);
        }
        _loadSimilarForViewer(assetId);
    } catch (e) { toast(e.message, 'error'); closeViewer(); }
}

function renderViewerMedia(asset) {
    const container = $('viewer-media');
    if (asset.file_type === 'VIDEO') {
        container.innerHTML = `<video src="${asset.file_url}" controls autoplay style="max-width:100%;max-height:100%"></video>`;
    } else {
        container.innerHTML = `<img src="${asset.file_url}" alt="${escHtml(asset.file_name)}">`;
    }
}

function updateViewerControls(asset) {
    const favBtn = $('viewer-favorite');
    favBtn.classList.toggle('active', asset.is_favorite);
}

async function deleteCurrentViewerAsset() {
    if (!state.viewerAsset) return;
    const deletedId = state.viewerAsset.id;
    await API.trashAsset(deletedId);
    toast(t('viewer.moved_to_trash'), 'success');

    // Drop it from the underlying grid so the page is already correct
    // once the viewer closes, without a full reload.
    const card = document.querySelector(`.photo-card[data-id="${deletedId}"]`);
    if (card) card.remove();

    const deletedIndex = state.viewerList.indexOf(deletedId);
    if (deletedIndex !== -1) state.viewerList.splice(deletedIndex, 1);

    if (!state.viewerList.length) {
        closeViewer();
        return;
    }
    // The next photo shifts into the deleted one's slot, so keeping the
    // same index (bounds-checked for "it was the last one") lands on it.
    state.viewerIndex = deletedIndex === -1 ? 0 : Math.min(deletedIndex, state.viewerList.length - 1);
    openViewer(state.viewerList[state.viewerIndex]);
}

function closeViewer() {
    $('viewer-overlay').classList.add('hidden');
    $('viewer-sidebar').classList.add('hidden');
    document.body.style.overflow = '';
    state.viewerAsset = null;

    // Hiding the overlay is just CSS (display:none) - it doesn't stop a
    // playing <video>, so its audio kept going in the background after
    // closing. Pausing isn't enough on its own either: some browsers keep
    // buffering/decoding a paused-but-still-loaded video, so the element
    // is removed from the DOM entirely.
    const video = $('viewer-media').querySelector('video');
    if (video) {
        video.pause();
        video.removeAttribute('src');
        video.load();
    }
    $('viewer-media').innerHTML = '';
}

function initViewer() {
    $('viewer-close').onclick = closeViewer;
    $('viewer-prev').onclick = () => navigateViewer(-1);
    $('viewer-next').onclick = () => navigateViewer(1);
    $('viewer-favorite').onclick = async () => {
        if (!state.viewerAsset) return;
        const r = await API.toggleFavorite(state.viewerAsset.id);
        state.viewerAsset.is_favorite = r.is_favorite;
        updateViewerControls(state.viewerAsset);
        toast(r.is_favorite ? t('viewer.added_to_favorites') : t('viewer.removed_from_favorites'), 'success');
    };
    $('viewer-download').onclick = () => {
        if (!state.viewerAsset) return;
        const a = document.createElement('a');
        a.href = state.viewerAsset.file_url;
        a.download = state.viewerAsset.file_name;
        a.click();
    };
    $('viewer-archive').onclick = async () => {
        if (!state.viewerAsset) return;
        const r = await API.toggleArchive(state.viewerAsset.id);
        toast(r.is_archived ? t('viewer.archived') : t('viewer.unarchived'), 'success');
        closeViewer();
        navigateTo(state.currentPage);
    };
    $('viewer-delete').onclick = deleteCurrentViewerAsset;
    $('viewer-similar-badge').onclick = _openSimilarFromBadge;
    $('viewer-info').onclick = () => {
        const sidebar = $('viewer-sidebar');
        sidebar.classList.toggle('hidden');
        if (!sidebar.classList.contains('hidden') && state.viewerAsset) {
            renderViewerInfo(state.viewerAsset);
        }
    };

    document.addEventListener('keydown', (e) => {
        if ($('viewer-overlay').classList.contains('hidden')) return;
        if (e.key === 'Escape') closeViewer();
        if (e.key === 'ArrowLeft') navigateViewer(-1);
        if (e.key === 'ArrowRight') navigateViewer(1);
        if (e.key === 'Delete') deleteCurrentViewerAsset();
    });

    _initViewerSwipe();
}

function _initViewerSwipe() {
    const media = $('viewer-media');
    let startX = 0, startY = 0, touching = false;

    media.addEventListener('touchstart', (e) => {
        if (e.touches.length !== 1) { touching = false; return; } // ignore pinch-zoom
        touching = true;
        startX = e.touches[0].clientX;
        startY = e.touches[0].clientY;
    }, { passive: true });

    media.addEventListener('touchend', (e) => {
        if (!touching) return;
        touching = false;
        const dx = e.changedTouches[0].clientX - startX;
        const dy = e.changedTouches[0].clientY - startY;
        const SWIPE_THRESHOLD = 50;
        if (Math.abs(dx) > SWIPE_THRESHOLD && Math.abs(dx) > Math.abs(dy) * 1.5) {
            navigateViewer(dx < 0 ? 1 : -1);
        }
    }, { passive: true });
}

async function navigateViewer(dir) {
    // Clamp instead of wrapping around - viewerList is often only whatever
    // the current page has loaded so far (the main gallery loads in pages
    // of 60 via infinite scroll), not the whole library, so wrapping past
    // the last loaded item jumped back to the first one instead of doing
    // nothing, which looked like it had looped partway through the library.
    let next = state.viewerIndex + dir;

    // Moving forward past everything the gallery has loaded so far, but
    // there's more on the server - fetch the next page instead of just
    // stopping there, so paging through photos doesn't force closing the
    // viewer, scrolling the grid to trigger a load, and reopening it.
    // Only the main gallery paginates viewerList this way; every other
    // page (albums/people/favorites/etc.) already loads its full list
    // up front, so state.gallery.totalPages > state.gallery.page is only
    // ever true there.
    if (dir > 0 && next >= state.viewerList.length &&
        state.currentPage === 'gallery' && state.gallery.totalPages > state.gallery.page) {
        state.gallery.page++;
        state.gallery.loading = false;
        await loadGalleryPage();
        next = state.viewerIndex + dir;
    }

    if (next < 0 || next >= state.viewerList.length) return;
    state.viewerIndex = next;
    openViewer(state.viewerList[state.viewerIndex]);
}

async function _runViewerReprocess(btn, apiCall, idleLabel, busyLabel) {
    btn.disabled = true;
    btn.textContent = busyLabel;
    try {
        const result = await apiCall();
        toast(result.message, 'success');
    } catch (e) {
        toast(e.message, 'error');
    } finally {
        btn.disabled = false;
        btn.textContent = idleLabel;
    }
}

