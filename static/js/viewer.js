// Purely a "what else looks like this?" browsing aid (precomputed CLIP
// embedding similarity, see similar_service.py) - never suggests deleting
// anything, so it only ever shows up when there's something to show.
// Drives both the floating badge above the close button and (if the info
// sidebar happens to be open) its "Benzer Fotoğraflar" strip, off one
// fetch instead of two.
let _viewerSimilarAssets = [];

async function _loadSimilarForViewer(assetId) {
    try {
        const data = await API.getSimilarAssets(assetId);
        // The viewer may have moved on to a different photo while this
        // request was in flight - stale results would land on the wrong
        // photo otherwise.
        if (!state.viewerAsset || state.viewerAsset.id !== assetId) return;
        _viewerSimilarAssets = data.assets || [];
        _updateSimilarBadge(_viewerSimilarAssets);
        _renderSimilarStrip(_viewerSimilarAssets);
    } catch (e) { /* non-critical - just don't show anything */ }
}

function _updateSimilarBadge(assets) {
    const badge = $('viewer-similar-badge');
    if (!badge) return;
    if (!assets.length) {
        badge.classList.add('hidden');
        return;
    }
    $('viewer-similar-badge-img').src = API.getThumb(assets[0].id, 'small');
    $('viewer-similar-badge-count').textContent = `+${assets.length}`;
    badge.classList.remove('hidden');
}

function _openSimilarFromBadge() {
    const sidebar = $('viewer-sidebar');
    if (sidebar.classList.contains('hidden')) {
        sidebar.classList.remove('hidden');
        if (state.viewerAsset) renderViewerInfo(state.viewerAsset);
    }
    const section = $('viewer-similar-section');
    if (section) section.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

// 2 rows of 4 before the rest folds into a "+N" cell - showing all
// SIMILAR_LIMIT (24) at once in a ~300px-wide sidebar would just be a
// wall of tiny squares, so only the first row-and-a-bit shows up front.
const SIMILAR_GRID_CAP = 8;

function _renderSimilarStrip(assets, expanded) {
    const section = $('viewer-similar-section');
    const strip = $('viewer-similar-strip');
    if (!section || !strip) return; // sidebar isn't open/rendered right now

    if (!assets.length) {
        section.style.display = 'none';
        return;
    }

    const showAll = expanded || assets.length <= SIMILAR_GRID_CAP;
    const shown = showAll ? assets : assets.slice(0, SIMILAR_GRID_CAP - 1);
    const overflow = assets.length - shown.length;

    strip.innerHTML = shown.map(a => `
        <div class="viewer-similar-cell" data-id="${a.id}" title="${escHtml(a.file_name)}">
            <img src="${API.getThumb(a.id, 'small')}" alt="" onerror="this.onerror=null;this.src='/static/broken-file.png'">
        </div>
    `).join('') + (overflow > 0 ? `
        <div class="viewer-similar-cell viewer-similar-more" id="viewer-similar-more">
            <img src="${API.getThumb(assets[shown.length].id, 'small')}" alt="" onerror="this.onerror=null;this.src='/static/broken-file.png'">
            <span class="viewer-similar-more-count">+${overflow}</span>
        </div>
    ` : '');
    section.style.display = '';

    qsa('.viewer-similar-cell[data-id]', strip).forEach(cell => {
        cell.onclick = () => openViewer(cell.dataset.id);
    });
    const moreCell = $('viewer-similar-more');
    if (moreCell) {
        moreCell.onclick = () => _renderSimilarStrip(assets, true);
    }
}
